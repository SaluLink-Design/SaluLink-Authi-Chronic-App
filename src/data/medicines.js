/**
 * Medical aid plan types
 */
export const planTypes = [
  "Core",
  "Priority",
  "Saver",
  "Executive",
  "Comprehensive"
];

/**
 * Get medicines for a specific condition
 * @param {string} condition - The condition name
 * @returns {Array} Array of medicine objects
 */
export const getMedicinesForCondition = (condition) => {
  return medicinesData[condition] || [];
};

/**
 * Filter medicines by plan type
 * @param {Array} medicines - Array of medicine objects
 * @param {string} planType - The plan type
 * @returns {Array} Filtered array of medicines
 */
export const filterMedicinesByPlan = (medicines, planType) => {
  if (!planType) return medicines;

  return medicines.map(medicine => {
    const isExcluded = medicine.planExclusions && 
      (medicine.planExclusions.toLowerCase().includes('not available on keycare') && 
       ['Core', 'Priority', 'Saver'].includes(planType));

    return {
      ...medicine,
      isExcluded,
      isSelectable: !isExcluded
    };
  });
};

/**
 * Get all conditions with medicines
 * @returns {Array} Array of condition names
 */
export const getAllMedicineConditions = () => {
  return Object.keys(medicinesData);
};

export default medicinesData;

